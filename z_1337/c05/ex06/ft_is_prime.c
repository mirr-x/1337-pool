int	ft_is_prime(int nb)
{
	int	i;
	int	flag;

	i = 2;
	flag = 0;
	if (nb <= 1)
	{
		return (0);
	}
	while (i != nb)
	{
		if (nb % i == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
