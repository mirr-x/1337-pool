#include <unistd.h>

int	main(int ac, char **av)
{
	char	*str;
	int		i;

	(void)ac;
	str = av[0];
	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
